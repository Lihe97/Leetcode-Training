func average(salary []int) float64 {
	min := math.MaxInt64
	max := 0
	var sum float64

	for i := 0 ; i < len(salary) ; i ++{
		if salary[i] > max{
			max = salary[i]
		}
		if salary[i] < min{
			min = salary[i]
		}
		sum += float64(salary[i])
	}
	//fmt.Println(sum)
	sum -= float64(min)
	sum -= float64(max)
	//fmt.Println(sum)

	return sum/float64(len(salary)-2)

}

